void  BenchFFT( void )
{
  int         i;
  int         k;
  FFT         fft;

  fft.N = OBJ_DATA_COUNT;
  fft.r = OBJ_DATA_SISU;
  fft.result_A = SpectA;
  fft.result_B = SpectB;

  for ( i= 0 ; i < FFT_TEST_COUNT ; i++ )
  {
      for( k= 0 ; k < fft.N ; k++ )
      {
          fft.result_A[k] = TestData[i+k];
      }
      digital_fft( &fft );
  }
  return;
}



